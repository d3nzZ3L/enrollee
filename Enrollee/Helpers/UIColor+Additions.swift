import Foundation
import UIKit
extension UIColor {
    class var neonBlue: UIColor {
        return UIColor(red: 36.0 / 255.0, green: 208.0 / 255.0, blue: 253.0 / 255.0, alpha: 1.0)
    }
    
    class var electricPurple: UIColor {
        return UIColor(red: 181.0 / 255.0, green: 35.0 / 255.0, blue: 255.0 / 255.0, alpha: 1.0)
    }
}

